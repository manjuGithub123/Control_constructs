
public class PolygonApp {

	public void side(int a)
	{
		switch(a)
		{
		case 5 :System.out.println("pentogon");break;
		case 6 :System.out.println("hexogon");break;
		case 7 :System.out.println("septagon");break;
		case 8 :System.out.println("octagon");break;
		case 9 :System.out.println("nanogon");break;
		
		default :System.out.println("not polygon");
		}
	}

}
